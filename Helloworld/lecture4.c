#include "lectures.h"
void lecture4()
{
	int num = 500;
	printf("지금 보유한 금액 : 450원 \n");
	printf("\"지금 보유한 금액 : %d원 \n", num);
	// 약속된 규칙으로 문자 출력을 할 수 없는 문자들이 있다.
	// 이러한 문자들을 출력하고 싶을 때 이스케이프 : "\"
	// \ + (약속된 문자) : 새로운 기능 구현
	// 종류 : \n, \a, \t, \b, \r, \\, \?, \", \'

	/*
	\n : 한 줄을 띄어준다. 개행. newline
	\a : alarm, 윈도우 경고음을 출력한다
	\t : tab문자 문자사이의 공간을 생성(tab) 기능
	\b : backspace, 콘솔 커서를 한 칸 뒤로 이동시킨다.
	\r : 캐리지 리턴(return), 마우스 커서를 해당 줄의 맨 처음으로 이동시킨다.
	\\, \?, \", \' : 시스템 약속 창 기본을 출력할 수 있는 기능
	*/

	// 변수를 이용한 데이터의 저장
	// 변수를 저장하는 다양한 방법
	// (1) 변수를 선언, 선언 할당
	int num1;
	num1 = 10;
	// (2) 변수를 초기화, (선언과 할당)
	int num2 = 20;
	// (3) 변수를 동시에 선언, 단 데이터 형태 같은 것만
	int num3, num4, num5;
	num3 = 10, num4 = 20, num5 = 30;
	printf("%d %d %d", num3, num4, num5);
	// (4) 변수를 동시에 선언과 함께 할당

	// 변수 이름 작성 시 제한 사항

	// 변수의 첫 글자에 숫자가 들어가면 안된다
	// int 776num;
	// #, $ 특수 기호를 이름을 사용할 수 없다
	// int #num;
	// 문자 이름 사이에 띄어쓰기(공백 분자)를 할 수 없다.
	// int your function;
	// 키워드도 이름으로 사용할 수 없다.
	// int int;

	// 변수에 이름을 잘 지을 것을 생각
}